package day4;

public class Subtraction {
	public static void main(String[] args) {
		int i = 20;
		int j = 13;

		int result = i - j;
		System.out.println("Result: " + result);

		//                 "Result 2: 2013"
		System.out.println("Result 2: " + i + j); // Result 2: 2013
		System.out.println("Result 3: " + (i + j)); // Result 3: 33                                
	}
}