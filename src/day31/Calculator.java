package day31;

// Class which consist of static methods is usually helper class
// These methods will not depend on class properties/instance variables
public class Calculator {
	
	public static int plus(int num, int num1) {
		return num + num1;
	}
	
	public static double plus(double num, double num1) {
		return num + num1;
	}
	
	public static int minus(int num, int num1) {
		return num - num1;
	}
	
	public static int mult(int num, int num1) {
		return num * num1;
	}
	
}
